    public static void SALVAR(Object o) {
        try {
            if (o == null) {
                return;
            }
            //Salva Concessionaria
            if (o instanceof Concessionaria) {
                Concessionaria c = (Concessionaria) o;
                ConcessionariaDAO cdao = (ConcessionariaDAO) AFactoryDao.getInstance(ConcessionariaDAO.class);
                cdao.salvar(c);
                cdao.commit();
                cdao.close();
            }

            if (o instanceof AntenaPista) {
                AntenaPista at = (AntenaPista) o;
                AntenaPistaDAO adao = (AntenaPistaDAO) AFactoryDao.getInstance(AntenaPistaDAO.class);
                adao.salvar(at);
                adao.commit();
                adao.close();
                return;
            }

            if (o instanceof ConfiguracaoPraca) {
                ConfiguracaoPracaDAO cpdao = (ConfiguracaoPracaDAO) AFactoryDao.getInstance(ConfiguracaoPracaDAO.class);
                ConfiguracaoPraca cp = (ConfiguracaoPraca) o;
                cpdao.salvar(cp);
                cpdao.commit();
                cpdao.close();
                return;
            }

            if (o instanceof ConfiguracaoImagem) {
                ConfiguracaoImagem conf = (ConfiguracaoImagem) o;
                ConfiguracaoImagemDAO cdao = (ConfiguracaoImagemDAO) AFactoryDao.getInstance(ConfiguracaoImagemDAO.class);
                cdao.salvar(conf);
                cdao.commit();
                cdao.close();
                return;
            }

            if (o instanceof TipoPista) {
                TipoPista tp = (TipoPista) o;
                TipoPistaDAO tdao = (TipoPistaDAO) AFactoryDao.getInstance(TipoPistaDAO.class);
                tdao.salvar(tp);
                tdao.commit();
                tdao.close();
                return;
            }

            if (o instanceof Categoria) {
                Categoria cat = (Categoria) o;
                CategoriaDAO cdao = (CategoriaDAO) AFactoryDao.getInstance(CategoriaDAO.class);;
                cdao.salvar(cat);
                cdao.commit();
                cdao.close();
                return;
            }

            //Salva Praca
            if (o instanceof Praca) {
                PracaDAO pdao = (PracaDAO) AFactoryDao.getInstance(PracaDAO.class);
                Praca p = (Praca) o;
                pdao.salvar(p);
                pdao.commit();
                pdao.close();
            }

            //Salva pista
            if (o instanceof Pista) {
                Pista p = (Pista) o;
                PistaDAO pdao = (PistaDAO) AFactoryDao.getInstance(PistaDAO.class);
                pdao.salvar(p);
                pdao.commit();
                pdao.close();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

