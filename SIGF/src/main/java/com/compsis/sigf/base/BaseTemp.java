    public static Object GET(int id, Class type, Object from) {
        if (type == Pista.class) {
            PistaDAO pdao = (PistaDAO) AFactoryDao.getInstance(PistaDAO.class);
            return pdao.obter(id);
        } else if (type == AntenaPista.class) {
            AntenaPistaDAO adao = (AntenaPistaDAO) AFactoryDao.getInstance(AntenaPistaDAO.class);
            return adao.obter(id);
        } else if (type == TipoPista.class) {
            TipoPistaDAO tpdao = (TipoPistaDAO) AFactoryDao.getInstance(TipoPistaDAO.class);
            return tpdao.obter(id);
        } else if (type == ConfiguracaoPraca.class) {
            ConfiguracaoPracaDAO cdao = (ConfiguracaoPracaDAO) AFactoryDao.getInstance(ConfiguracaoPracaDAO.class);
            return cdao.obter(id);
        } else if (type == Praca.class) {
            PracaDAO pdao = (PracaDAO) AFactoryDao.getInstance(PracaDAO.class);
            return pdao.obter(id);
        } else if (type == Concessionaria.class) {
            ConcessionariaDAO cdao = (ConcessionariaDAO) AFactoryDao.getInstance(ConcessionariaDAO.class);
            return cdao.obter(id);
        } else if (type == Categoria.class) {
            CategoriaDAO cdao = (CategoriaDAO) AFactoryDao.getInstance(CategoriaDAO.class);;
            return cdao.obter(id);
        }
        return null;
    }

